    async def _attempt_recoveries(self, report: MedicReport):
        """Attempt automatic recovery for failed/degraded components."""
        for component_name, health in report.component_statuses.items():
            if health.status in [HealthStatus.FAILED, HealthStatus.DEGRADED]:
                # Attempt recovery based on component type
                recovery_result = await self._recover_component(component_name, health)

                if recovery_result["attempted"]:
                    report.recoveries_attempted += 1
                    health.recovery_attempted = True

                    if recovery_result["successful"]:
                        report.recoveries_successful += 1
                        health.recovery_successful = True
                        health.status = HealthStatus.HEALTHY
                        health.last_healthy = datetime.utcnow()
                        health.failure_count = 0
                        report.actions_taken.append(f"Recovered {component_name}: {recovery_result['action']}")
                    else:
                        report.recoveries_failed += 1
                        health.recovery_successful = False
                        health.failure_count += 1
                        report.actions_taken.append(f"Failed to recover {component_name}: {recovery_result['reason']}")

    async def _recover_component(self, component: str, health: ComponentHealth) -> Dict[str, Any]:
        """
        Attempt bounded recovery for a component.

        Returns dict with: attempted, successful, action, reason
        """
        logger.info(f"Attempting recovery for {component}")

        # Recovery strategies by component type
        if component == "runtime_continuity":
            # Could try: reinitialize state, reload config, etc.
            return {
                "attempted": True,
                "successful": False,
                "action": "state_reinit",
                "reason": "Auto-recovery not yet implemented"
            }

        # Default: no recovery available
        return {
            "attempted": False,
            "successful": False,
            "action": None,
            "reason": "No recovery strategy defined"
        }

    async def _escalate_critical_issues(self, report: MedicReport):
        """Escalate critical issues that require human attention."""
        critical_components = [
            name for name, health in report.component_statuses.items()
            if health.status == HealthStatus.FAILED and health.failure_count >= self.max_failures_before_escalation
        ]

        if critical_components:
            escalation = {
                "timestamp": datetime.utcnow().isoformat(),
                "severity": "critical",
                "components": critical_components,
                "message": f"Critical system failures in: {', '.join(critical_components)}",
                "report_id": report.cycle_id
            }

            report.escalations.append(escalation)

            # Send alert through gateway if available
            if self.gateway:
                await self.gateway.send_alert(
                    severity="critical",
                    message=escalation["message"],
                    details=escalation
                )

            logger.critical(f"Escalating critical issues: {critical_components}")

    async def get_health_summary(self) -> Dict[str, Any]:
        """Get current health summary."""
        if not self.last_report:
            return {
                "status": "unknown",
                "message": "No medic report available yet"
            }

        return {
            "status": "healthy" if self.last_report.system_healthy else "unhealthy",
            "last_check": self.last_run.isoformat() if self.last_run else None,
            "components_healthy": self.last_report.components_healthy,
            "components_degraded": self.last_report.components_degraded,
            "components_failed": self.last_report.components_failed,
            "issues": self.last_report.issues_found,
            "escalations": len(self.last_report.escalations)
        }
